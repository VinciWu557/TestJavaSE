public class HiWorld {
    public static void main(String[] args) {
        //自我介绍:
        System.out.print("姓名:");
        System.out.print("\t丽丽\n");
        System.out.print("职业:");
        System.out.print("\t学生");

        /*
        1.System.out.println与System.out.print区别:
        System.out.println:将双引号中的内容原样输出,换行
        System.out.print:将双引号中的内容原样输出,不换行

        2.转义字符:
        \ >>作用:将普通的字母转换为特殊含义
        \t:距离前面有一个制表符位置(代表8位)
        \n:换行

         */
        System.out.println();
        System.out.println("1111111111111111111");
        System.out.println("11111111\t2222");

    }
}
